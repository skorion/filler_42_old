#include "libft.h"

int		ft_atoi(const char *str)
{
	long long int i[4];

	i[1] = 0;
	i[2] = 0;
	i[3] = 1;
	while ((str[i[1]] >= 9 && str[i[1]] <= 13) || str[i[1]] == ' ')
		i[1]++;
	if (str[i[1]] == '+' || str[i[1]] == '-')
	{
		if (str[i[1]] == '-')
			i[3] = -1;
		i[1]++;
	}
	while (str[i[1]] >= '0' && str[i[1]] <= '9')
	{
		i[2] = i[2] * 10 + (str[i[1]] - '0');
		i[1]++;
	}
	if (i[2] * i[3] > 0)
		if (i[3] < 0)
			return (0);
	if (i[2] * i[3] < 0)
		if (i[3] > 0)
			return (-1);
	return ((int)(i[2] * i[3]));
}
